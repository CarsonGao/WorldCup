package wc.com.worldcup;

public class BeautifulGileItem {

    private String name;

    private int imageId;

    public BeautifulGileItem(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}